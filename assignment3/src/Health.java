public class Health {

    private int pool;
    private int amount;

    public Health(int pool) {
        this.pool = pool;
        this.amount = pool;
    }

    public int getPool() {
        return pool;
    }

    public void setPool(int pool) {
        this.pool = pool;
    }

    public int getAmount() {
        return amount;
    }

    public void setAmount(int amount) {
        this.amount = amount;
    }


}
